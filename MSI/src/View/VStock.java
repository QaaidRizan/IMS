    private void txt_supplieridActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_supplieridActionPerformed

        int supplierid =Integer.parseInt(txt_supplierid.getText());
        if (supplierid==0) {
            JOptionPane.showMessageDialog(null, "Please enter the supplier ID to view details");
            return;
        }

        try {
            Connection conn = MDBConnection.createConnection();
            String sql = "SELECT SupplierName FROM supplier WHERE SupplierID = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, supplierid);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                // Assuming these text fields are defined and used for displaying member details

                txt_suppliername.setText(rs.getString("SupplierName"));

            } else {
                JOptionPane.showMessageDialog(null, "Supplier ID not found");
            }

            conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error retrieving Supplier details: " + e.getMessage());
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_txt_supplieridActionPerformed

    private void txt_productidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_productidActionPerformed

        int productid = Integer.parseInt(txt_productid.getText());

        if (productid == 0) {
            JOptionPane.showMessageDialog(null, "Please enter the product ID to view details");
            return;
        }

        try {
            try (Connection conn = MDBConnection.createConnection()) {
                String sql = "SELECT ProductName,Qty,Price FROM Product WHERE ProductID = ?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setInt(1, productid);
                ResultSet rs = pst.executeQuery();

                if (rs.next()) {
                    txt_productname.setText(rs.getString("ProductName"));
                    int unitprice = rs.getInt("Price");
                    txt_unitprice.setText(String.valueOf(unitprice));
                    int currentqty = rs.getInt("Qty");
                    txt_currentqty.setText(String.valueOf(currentqty));

                } else {
                    JOptionPane.showMessageDialog(null, "Product ID not found");
                }       }
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error retrieving product details: " + e.getMessage());
            }

            // TODO add your handling code here:
    }//GEN-LAST:event_txt_productidActionPerformed


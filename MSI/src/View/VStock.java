    private void txt_returnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_returnActionPerformed
        int qty = Integer.parseInt(txt_qty.getText());
int productid = Integer.parseInt(txt_productid.getText());

if (qty == 0) {
    JOptionPane.showMessageDialog(null, "Please enter a valid quantity to return");
    return;
}

try {
    Connection conn = MDBConnection.createConnection();
    String sqlProduct = "SELECT Qty FROM Product WHERE ProductID = ?";
    PreparedStatement pstProduct = conn.prepareStatement(sqlProduct);
    pstProduct.setInt(1, productid);
    ResultSet rs = pstProduct.executeQuery();

    if (rs.next()) {
        int currentQty = rs.getInt("Qty");

        if (currentQty < qty) {
            JOptionPane.showMessageDialog(null, "Return quantity exceeds current stock!");
            return;
        }

        // Calculate the new quantity after the return
        int newQty = currentQty - qty;

        // Update the product quantity in the Product table
        String updateQtySQL = "UPDATE Product SET Qty = ? WHERE ProductID = ?";
        PreparedStatement pstUpdate = conn.prepareStatement(updateQtySQL);
        pstUpdate.setInt(1, newQty);
        pstUpdate.setInt(2, productid);
        pstUpdate.executeUpdate();

        // Insert return details into the Return table
        String insertReturnSQL = "INSERT INTO ReturnStock (StockID, ProductID, SupplierID, ReturnDate, ReturnQty, Price) VALUES (?, ?, ?, CURDATE(), ?, ?)";
PreparedStatement pstReturn = conn.prepareStatement(insertReturnSQL);
pstReturn.setInt(1, Integer.parseInt(txt_stockid.getText()));
pstReturn.setInt(2, Integer.parseInt(txt_productid.getText()));
pstReturn.setInt(3, Integer.parseInt(txt_supplierid.getText()));
pstReturn.setInt(4, qty);
pstReturn.setInt(5, Integer.parseInt(txt_unitprice.getText()));  // Assuming Price comes from txt_unitprice
pstReturn.executeUpdate();


        JOptionPane.showMessageDialog(null, "Stock returned successfully and Product Qty updated");

    } else {
        JOptionPane.showMessageDialog(null, "Product ID not found");
    }

    conn.close();
} catch (SQLException e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
}
        clearField();


        // TODO add your handling code here:
    }//GEN-LAST:event_txt_returnActionPerformed


    public void id_autoincrement(){
    
           try {
            Connection conn = MDBConnection.createConnection();
            String sql = "SELECT COALESCE(MAX(ProductID), 0) FROM Product";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            int nextId = 1; // Default ID if no records are found

            if (rs.next()) {
                int maxId = rs.getInt(1);
                nextId = maxId + 1;
            }

            txt_productid.setText(String.valueOf(nextId));
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    

private boolean validateProductId() {
    String productIdText = txt_productid.getText().trim();
    if (productIdText.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Product ID cannot be empty");
        return false;
    }
    try {
        Integer.parseInt(productIdText);
        return true;
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Product ID must be a valid number");
        return false;
    }
}

private boolean validateProductName() {
    if (txt_productname.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Product Name cannot be empty");
        return false;
    }
    return true;
}

private boolean validatePrice() {
    String priceText = txt_price.getText().trim();
    if (priceText.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Price cannot be empty");
        return false;
    }
    try {
        int price = Integer.parseInt(priceText);
        if (price <= 0) {
            JOptionPane.showMessageDialog(null, "Price must be greater than zero");
            return false;
        }
        return true;
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Price must be a valid number");
        return false;
    }
}

private boolean validateDates() {
    if (date_create.getDate() == null) {
        JOptionPane.showMessageDialog(null, "Create Date cannot be empty");
        return false;
    }
    if (date_modify.getDate() == null) {
        JOptionPane.showMessageDialog(null, "Modify Date cannot be empty");
        return false;
    }
    if (date_warranty.getDate() == null) {
        JOptionPane.showMessageDialog(null, "Warranty Date cannot be empty");
        return false;
    }
    if (date_create.getDate().after(date_modify.getDate())) {
        JOptionPane.showMessageDialog(null, "Create Date cannot be after Modify Date");
        return false;
    }
    return true;
}

